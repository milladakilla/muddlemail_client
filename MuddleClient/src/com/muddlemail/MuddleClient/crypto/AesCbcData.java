package com.muddlemail.MuddleClient.crypto;


/**
 *
 * @author matt
 */
public class AesCbcData {
///////////////////////////////////////////////////////////////////////////////
// Class Variables ////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
    protected byte[] dataCipher;
    protected byte[] iv;
    
///////////////////////////////////////////////////////////////////////////////
// Constructors ///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
    public AesCbcData(byte[] cipherData, byte[] iv) {
        this.dataCipher = cipherData;
        this.iv = iv;
    }
    
///////////////////////////////////////////////////////////////////////////////
// Methods ////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////    
    /**
     * 
     * @return
     */
    public byte[] getDataCipher() {
        return dataCipher;
    }
    
    /**
     * 
     * @return
     */
    public byte[] getIv() {
        return iv;
    }
}