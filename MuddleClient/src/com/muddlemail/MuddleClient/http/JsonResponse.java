package com.muddlemail.MuddleClient.http;

/**
 * 
 * @author matt
 *
 */
public class JsonResponse {
///////////////////////////////////////////////////////////////////////////////
// Class Variables ////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
	protected String jsonData;
	protected String httpReturnCode;
	
///////////////////////////////////////////////////////////////////////////////
// Constructors ///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
	/**
	 * 
	 * @param jsonData
	 * @param httpReturnCode
	 */
	public JsonResponse (String jsonData, int httpReturnCode) {
		this.jsonData = jsonData;
		this.httpReturnCode = Integer.toString(httpReturnCode);
	}
	
///////////////////////////////////////////////////////////////////////////////
// Methods ////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
	/**
	 * 
	 * @return
	 */
	public String getJson() {
		return jsonData;
	}
	
	/**
	 * 
	 * @return
	 */
	public String getHttpReturnCode() {
		return httpReturnCode;
	}
}
